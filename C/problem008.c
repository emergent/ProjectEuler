int chrtoi(char c) { return (int)(c - 48); }

void problem008() {
    long ans, res;
    int i, j, maxlen;
    char* target = DIGIT1000;
    int len = 13;

    /* solve this problem here */
    maxlen = strlen(target);
    res = 1;
    ans = 1;
    for (i = 0; i < maxlen - len; i++) {
        for (j = 0; j < len; j++) {
            res *= chrtoi(target[i + j]);
        }
        if (res > ans) {
            ans = res;
        }
        res = 1;
    }

    printf("%s: answer = %ld\n", __FUNCTION__, ans);
}

